package com.gyejoong.mdmuserweb.vo;

public class ManagementVo {
	private String Active;
	private String Wifi;
	private String Bluetooth;
	private String Tethering;
	private String Camera;
	private String VoiceRecord;
	private String Alarm;
	private String Gps;
	public String getActive() {
		return Active;
	}
	public void setActive(String active) {
		Active = active;
	}
	public String getWifi() {
		return Wifi;
	}
	public void setWifi(String wifi) {
		Wifi = wifi;
	}
	public String getBluetooth() {
		return Bluetooth;
	}
	public void setBluetooth(String bluetooth) {
		Bluetooth = bluetooth;
	}
	public String getTethering() {
		return Tethering;
	}
	public void setTethering(String tethering) {
		Tethering = tethering;
	}
	public String getCamera() {
		return Camera;
	}
	public void setCamera(String camera) {
		Camera = camera;
	}
	public String getVoiceRecord() {
		return VoiceRecord;
	}
	public void setVoiceRecord(String voiceRecord) {
		VoiceRecord = voiceRecord;
	}
	public String getAlarm() {
		return Alarm;
	}
	public void setAlarm(String alarm) {
		Alarm = alarm;
	}
	public String getGps() {
		return Gps;
	}
	public void setGps(String gps) {
		Gps = gps;
	}
}
